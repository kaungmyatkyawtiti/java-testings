package com.nottnott.app.chapter7;

/**
 * MethodDemo
 */
public class MethodDemo {
  static void hello() {
    System.out.println("Hello");
    System.out.println("Hi");
    System.out.println("Morning");
  }

  public static void main(String[] args) {
    hello();
    System.out.println("___________");

    hello();
    System.out.println("___________");

    hello();
  }
}
